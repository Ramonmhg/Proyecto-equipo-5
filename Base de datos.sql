-- Crear tabla ENTRADA

CREATE TABLE ENTRADA (
    ID_EMPLEADO INT NOT NULL,
    ENTRADA DATETIME NOT NULL,
    PRIMARY KEY (ID_EMPLEADO, ENTRADA),
